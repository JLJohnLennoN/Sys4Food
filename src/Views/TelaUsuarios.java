    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
      int resposta = 0;
      resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja realmente excluir este usuário?");
      if (resposta == JOptionPane.YES_OPTION){
          usuario.setMatricula(Integer.parseInt(txtMatricula.getText()));
          dao.delete(usuario);
            txtUsuario.setText("");
            txtSenha.setText("");
            txtConfimarSenha.setText("");
            txtMatricula.setText("");
            txtBuscar.setText("");
            comboBoxTipo.setToolTipText("Selecione");
      }
    }//GEN-LAST:event_btExcluirActionPerformed

