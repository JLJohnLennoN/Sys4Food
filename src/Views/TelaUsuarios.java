    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        
        if(txtUsuario.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Informe o campo Usuário para continuar");
            txtUsuario.requestFocus();
        } else if(txtConfimarSenha.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o campo Confirmar Senha para continuar");
            txtConfimarSenha.requestFocus();
        }else if(txtSenha.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o campo Senha para continuar");
            txtSenha.requestFocus();
        }else if(txtSenha.getText().equals(txtConfimarSenha.getText())){
            if (flag==1){
                
            usuario.setNome(txtUsuario.getText());
            usuario.setTipo((String)comboBoxTipo.getSelectedItem());
            usuario.setSenha(txtSenha.getText());
            dao.save(usuario);
            txtUsuario.setText("");
            txtSenha.setText("");
            txtConfimarSenha.setText("");
            
            txtUsuario.setEnabled(false);
            txtSenha.setEnabled(false);
            txtConfimarSenha.setEnabled(false);
            comboBoxTipo.setEnabled(false);
            btSalvar.setEnabled(false);
            txtBuscar.setEnabled(true);
            btBuscar.setEnabled(true);
        }else{
            usuario.setNome(txtUsuario.getText());
            usuario.setTipo((String)comboBoxTipo.getSelectedItem());
            usuario.setSenha(txtSenha.getText());
            dao.update(usuario);
            txtUsuario.setText("");
            txtSenha.setText("");
            txtConfimarSenha.setText("");
            
            txtUsuario.setEnabled(false);
            txtSenha.setEnabled(false);
            txtConfimarSenha.setEnabled(false);
            comboBoxTipo.setEnabled(false);
            btSalvar.setEnabled(false);
            }
        }else
            JOptionPane.showMessageDialog(null, "As senha não correspondem");
    }//GEN-LAST:event_btSalvarActionPerformed

    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        usuario.setPesquisa(txtBuscar.getText());
        Usuarios model = dao.buscaUsuarios(usuario);
        txtUsuario.setText(model.getNome());
        txtSenha.setText(model.getSenha());
        txtConfimarSenha.setText(model.getSenha());
        comboBoxTipo.setSelectedItem(model.getTipo());
        txtMatricula.setText(String.valueOf(model.getMatricula()));
        btAlterar.setEnabled(true);
        btExcluir.setEnabled(true);
    }//GEN-LAST:event_btBuscarActionPerformed

